using System;



class FloarOrDouble
{
    static void Main()
    {
        double num1 = 34.567839023;
        float num2 = 12.345f;
        double num3 = 8923.1234857;
        float num4 = 3456.091f;

        Console.WriteLine("{0} is of type double\n{1} is of type float\n{2} is of type double\n{3} is of type float", num1, num2, num3, num4);
    }
}

